/*=============================================================================
* Includes
=============================================================================*/
using UnityEngine;
public class MainMenuText : MonoBehaviour {
    /*=============================================================================
    * Class Variables
    =============================================================================*/

    /*=============================================================================
    * Unity Callbacks 
    =============================================================================*/
    void OnEnable() => GameManager.on_move_to_game += DisableMainMenu;
    void OnDisable() => GameManager.on_move_to_game -= DisableMainMenu;

    /*=============================================================================
    * Class Methods
    =============================================================================*/
    void DisableMainMenu() {
        gameObject.SetActive(false);
    }

}