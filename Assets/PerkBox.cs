/*=============================================================================
* Includes
=============================================================================*/
using UnityEngine;
public class PerkBox : MonoBehaviour {
    public enum PerkType {
        WidenPaddle,
    }
    /*=============================================================================
    * Class Variables
    =============================================================================*/
    PerkType perk;
    PerkType Perk => perk;
    Rigidbody2D rb;
    [SerializeField] float falldown_speed = -5f;

    /*=============================================================================
    * Unity Callbacks 
    =============================================================================*/
    void Start() {
        rb = GetComponent<Rigidbody2D>();
        rb.linearVelocity = new Vector2(0, falldown_speed);
    }

    /*=============================================================================
    * Class Methods
    =============================================================================*/

}